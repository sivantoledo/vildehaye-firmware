uint32_t ratDiffToUs(uint32_t now, uint32_t rat_time) {
  if (now > rat_time) {
  	return ((UINT_MAX - now) + rat_time) / 4; // microseconds to wrap around, because RAT runs at 4MHz
  } else {
  	return (rat_time - now) / 4;
  }
}

