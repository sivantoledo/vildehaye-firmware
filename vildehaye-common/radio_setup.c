#endif
		break;
	case DATA_ENCODING_PLAIN:
	default:
		System_printf("enc plain\n");
		radio_cmd_prop_div_setup[index].formatConf.fecMode = 0;
    //radio_cmd_prop_div_setup[index].pRegOverride = gfskOverrides;
